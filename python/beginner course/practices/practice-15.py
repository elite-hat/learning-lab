for item in 'AbdunNafayRathore':                                # Characters
    print(item)
for item in ['person1', 'person2', 'person3', 'person4']:       # Strings
    print(item)
for item in [1, 2, 3, 4, 5]:                                    # Numbers
    print(item)
for item in range(10):                                          # From 0 to 10
    print(item)
for item in range(1, 5):                                        # From 1 to 4
    print(item)
for item in range(1, 10, 2):                                    # From 1 to 10 with the gap of 2
    print(item)